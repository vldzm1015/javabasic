package b_operator;
/*
 *  shift : 모든 비트의 값을 이동하는 연산자
 *  
 *  >> : 오른쪽 이동
 *  << : 왼쪽으로 이동
 *  <<< : 오른쪽 이동 ( 부호에 0 )
 */

public class Ex07_Shift {

	public static void main(String[] args) {

		int a = -4 , b = 5;
		System.out.println(a>>2);
		System.out.println(a<<1);
		System.out.println(a>>>2);
		System.out.println(b>>2);
		System.out.println(b<<1);
	}

}
