package a_datatype;

public class Ex03_Declaration {
	
	public static void main(String[] args) {
		// 1. 변수선언 -> 값 대입
		int kor;
		kor = 30;
		//2.초기화 = 변수선언 + 값지정(대입)
		int eng = 30;
		if(kor == eng) {
			System.out.println("같다");
		}else {
			System.out.println("다르다");
		}
		
		int temp;
		temp = kor;
		kor = eng;
		eng = temp;
		System.out.println("국어 : " + kor + ", 영어 : " + eng);

	}

}
