package a_datatype;

/*
 * 데이타타입(자료형)
 * 1. 기본형(프리미터브)
 * 		논리형 : boolean
 * 		문자형 : char
 * 		정수형 : int / long
 * 		실수형 : double
 * 2. 참조형
 */


public class Ex01_Naming {


 public static void main (String[] args) {
		// 1. 변수선언
		char a;
		int b;
		double d;
		
		// 2. 값 지정
		a = '김';        //    char형에는 ''으로 문자 하나만 지정
		b = 100;
		d = 100.3;
	System.out.println(a);
	}
	 
}
	

