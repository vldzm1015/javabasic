package g_access.sub;

public class Access {
	private String a = "퍼블릭 데이타";
	public String b = "퍼블릭 데이터";
	 String c= "디폴트 데이터";
	protected String d = "프로텍티드 데이터";
	
	public void output() {
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
	}
	
}
/*접근 지정자
 * 	-private : 해당 클래스에만 허용
 * 	-public : 모든 허용(다른패키지도 접근가능)
 * 	-default(아무 지정 없는 경우) : 해당 패키지에만 허용
 * 	-protected : 다른 패키지에 있는 경우 상속관계에만 허용
 * 				  동일한 패키지에는 그냥 허용
 * 
 * private < default < protected < public
 */
