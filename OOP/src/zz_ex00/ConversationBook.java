package zz_ex00;

public class ConversationBook extends Book {
	String language;
	public ConversationBook(){
		super();
	}
	ConversationBook(int productID, String description, String producer, int price, 
					 int ISBN, String author, String title, String language) {
		super(productID, description, producer, price, ISBN, author, title);
		this.language=language;
	}
	public void showInfo() {
		System.out.println("상품ID : "+productID);
		System.out.println("상품설명 : "+description);
		System.out.println("생산자 : "+producer);
		System.out.println("가격 : "+price);
		System.out.println("ISBN : "+ISBN);
		System.out.println("저자 : "+author);
		System.out.println("제목 : "+title);
		System.out.println("언어 : "+language);
	}
}
